     private void formatarColumnJTable() {
        FontMetrics fm = jTbUsuario.getFontMetrics(jTbUsuario.getFont());
        jTbUsuario.setColumnModel(new UsuarioColumnModel(fm));
    }
    
    UsuarioTableModel model = new UsuarioTableModel();
    public void setListaUsuario(List<Usuario> listaUsuario) {        
        this.jTbUsuario.setModel(model);        
        model.addListaDeUsuario(listaUsuario);
        this.formatarColumnJTable();
    }
    
    public Usuario getUsuarioSelecionado() {
        return model.getUsuario(jTbUsuario.getSelectedRow());
    }
    
    public String[] getParametroPesquisa() {
        String param[] = new String[2];
        param[0] = (String)jCboPesquisarPor.getSelectedItem();
        param[1] = jTxfPesquisarValor.getText().toUpperCase();
        return param;
    }
    
