    private LoginControler loginControler;

    private void logar() {
        try {
            if (loginControler == null) {
                loginControler = new LoginControler();
            }
            if (jTxfUsuario.getText().equals("")) {
                throw new Exception("Informe um Usuario");
            }

            if (jPwfSenha.getPassword().length == 0) {
                throw new Exception("Senha n√£o Informada");
            }
            String userName = jTxfUsuario.getText();
            String senha = Uteis.parseSenha(jPwfSenha.getPassword());

            Usuario usuarioLogado = loginControler.logar(userName, senha);

            TelaPrincipal telaPrincipal = new TelaPrincipal();
            telaPrincipal.setUsuario(usuarioLogado);
           
            telaPrincipal.setVisible(true);
            
            dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Aten√ß√£o", JOptionPane.INFORMATION_MESSAGE);
        }

    }

     
    
    
    
